    private void jButtonSaveEmpleadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonSaveEmpleadoMouseClicked
        if (JTextNombreEmpleado.getText().isBlank() || JTextNombreEmpleado.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Usted debe digitar el nombre");
            return;
        }
        
        if (jTextFieldId.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Ingrese una Identificacion");
            return;
        }
                
        try {
            int id = Integer.parseInt(jTextFieldId.getText());
            if (id > 999999999) {
                JOptionPane.showMessageDialog(null, "Identificacion no Valida");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor numerico valido en Identificacion");
            return;
        }
        
        if (jTextFieldEdad.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Ingrese una Edad");
            return;
        }
        
        if (jComboBoxGenero.getSelectedItem().toString().startsWith("---")) {
            JOptionPane.showMessageDialog(null, "Seleccione un Genero");
            return;
        }
        
        if (jComboBoxCargo.getSelectedItem().toString().startsWith("---")) {
            JOptionPane.showMessageDialog(null, "Seleccione un Cargo");
            return;
        }
        
        if (jTextFieldSalario.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Ingrese un salario");
            return;
        }

        if (Integer.parseInt(jTextFieldSalario.getText()) < 1000000) {
            JOptionPane.showMessageDialog(null, "Ingrese un salario correcto");
            return;
        }
        
        if (jDateChooserEmpleado.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Ingrese la Fecha de Su Ingreso");
            return;
        }
        
       
        if (jTextFieldJefe.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Ingrese el Nombre del Jefe");
            return;
        }

        Empleado objEmpleado = new Empleado();
        objEmpleado.setNombre(JTextNombreEmpleado.getText());
        objEmpleado.setCargo(jComboBoxCargo.getSelectedItem().toString());
        objEmpleado.setIdentificacion(Integer.parseInt(jTextFieldId.getText()));
        objEmpleado.setGenero(jComboBoxGenero.getSelectedItem().toString());
        objEmpleado.setSalario(Double.parseDouble(jTextFieldSalario.getText()));
        
        Date fechaIngreso = jDateChooserEmpleado.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String fechaIngresoSring = sdf.format(fechaIngreso);
        objEmpleado.setFechaIngreso(fechaIngresoSring);
        objEmpleado.setNombreJefe(jTextFieldJefe.getText());
        objEmpleado.setEdad(Integer.parseInt(jTextFieldEdad.getText()));
        
        
        if(objEmpleado.existeEmpleado(objEmpleado.getIdentificacion())){
            JOptionPane.showMessageDialog(null, "El dato existe");
            return;
        }
        
        if(objEmpleado.almacenarDatosEmpleado(objEmpleado)){
            JOptionPane.showMessageDialog(null, "El Empleado fue Guardado");
            ClearScreen();
        }

    }//GEN-LAST:event_jButtonSaveEmpleadoMouseClicked
    
