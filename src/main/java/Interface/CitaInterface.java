    public String fechaToString(Date fechaDate) {
        
        SimpleDateFormat fechaok = new SimpleDateFormat("dd/MM/yyyy");
        String fechaFormat = fechaok.format(fechaDate);
        
        return fechaFormat;
    }

    private void jButtonFindMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonFindMouseClicked
        if (jTextFieldDocumentoPaciente.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Ingrese una Identificacion");
            return;
        }

        try {
            int id = Integer.parseInt(jTextFieldDocumentoPaciente.getText());
            if (id > 999999999) {
                JOptionPane.showMessageDialog(null, "Identificacion no Valida");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor numerico valido en Identificacion");
            return;
        }

        Paciente objPaciente = new Paciente();
        objPaciente = objPaciente.getPacientexId(Integer.parseInt(jTextFieldDocumentoPaciente.getText()));

        if (objPaciente != null) {
            String datosPaciente = "Nombre:" + objPaciente.getNombre() + "\n" + "Direccion: " + objPaciente.getDireccion() + "\n" + "Edad: " + objPaciente.getEdad();
            pacienteCita = objPaciente;
            jTextPaneDatosPaciente.setText(datosPaciente);

        } else {
            JOptionPane.showMessageDialog(null, "El paciente no existe");
            jTextFieldDocumentoPaciente.setText("");
            jTextPaneDatosPaciente.setFocusable(rootPaneCheckingEnabled);
        }

        jTextPaneVlrCita.setText(Double.toString(objPaciente.pagoConsulta()));


    }//GEN-LAST:event_jButtonFindMouseClicked

