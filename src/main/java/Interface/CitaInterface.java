    private void jButtonSaveCitaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonSaveCitaMouseClicked
        if (jTextFieldDocumentoPaciente.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Ingrese una Identificacion");
            return;
        }

        if (jDateChooserCita.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Ingrese la Fecha de la consulta");
            return;
        }

        if (timePickerCita.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Ingresa una Hora para su Consulta");
            return;
        }

        if (jComboBoxConsultorio.getSelectedItem().toString().startsWith("---")) {
            JOptionPane.showMessageDialog(null, "Seleccione un Consultorio");
            return;
        }

        if (jComboBoxMedicoCita.getSelectedItem().toString().startsWith("---")) {
            JOptionPane.showMessageDialog(null, "Seleccione un Medico");
            return;
        }

        if (jTextFieldDocumentoPaciente.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Ingrese una Identificacion");
            return;
        } else {
            String datosCita = "Nombre del Paciente:" + pacienteCita.getNombre() + "\n" + "Identificacion: " + pacienteCita.getDireccion()
                    + "\n" + "Fecha Cita: " + fechaToString(jDateChooserCita.getDate()) + "\n" + "Hora Cita:" + timePickerCita.getText();

            jTextAreaMedico.setText(datosCita);
        }

        try {
            int id = Integer.parseInt(jTextFieldDocumentoPaciente.getText());
            if (id > 999999999) {
                JOptionPane.showMessageDialog(null, "Identificacion no Valida");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor numerico valido en Identificacion");
            return;
        }

        Cita objCita = new Cita();
        objCita.setPaciente(this.pacienteCita);
        objCita.setConsultorio(jComboBoxConsultorio.getSelectedItem().toString());
        objCita.setFecha(fechaToString(jDateChooserCita.getDate()));
        objCita.setHora(timePickerCita.getText());
        objCita.setConsultorio(jComboBoxConsultorio.getSelectedItem().toString());
        Medico objMedico = new Medico();
        objMedico.setNombre(jComboBoxMedicoCita.getSelectedItem().toString());
        objCita.setMedico(objMedico);
        objCita.setValorCita(Double.parseDouble(jTextPaneVlrCita.getText()));
        objCita.setMedicoNombre(jComboBoxMedicoCita.getSelectedItem().toString());

        if (objCita.existeCitaPaciente(objCita)) {
            JOptionPane.showMessageDialog(null, "El Paciente tiene una cita agendada para ese dia");
            return;
        }
        
        if(objCita.existeMedicoDisponible(objCita)){
            JOptionPane.showMessageDialog(null, "El medico no esta disponible en esa hora y fecha");
            return;
        }
        
        if (objCita.almacenarDatosCita(objCita)) {
            JOptionPane.showMessageDialog(null, "La Cita fue Guardado");

        }


    }//GEN-LAST:event_jButtonSaveCitaMouseClicked

